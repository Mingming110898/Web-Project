    <td><strong>2. Tactical </strong></td>
    <td> A project rated at this level affects multiple services within a program and may involve more significant procurement activities. It may involve some information management or information technology (IM/IT) or engineering activities. The project risk profile may indicate that some risks could have serious impacts, requiring carefully planned responses. The scope of a tactical project is operational in nature and delivers new capabilities within limits.</td>
    <td>45 to 63</td>
    </tr>

    <tr>
    <td><strong>3. Evolutionary </strong></td>
    <td>As indicated by the name, projects within this level of complexity and risk introduce change, new capabilities and may have a fairly extensive scope. Disciplined skills are required to successfully manage evolutionary projects. Scope frequently spans programs and may affect one or two other departments or agencies. There may be substantial change to business process, internal staff, external clients, and technology infrastructure. IM/IT components may represent a significant proportion of total project activity. </td>
    <td>64 to 82</td>
    </tr>
    
    <tr>
